                    <div class="card-body">
                        <p class="text-start">Sistem pakar termasuk kedalam kelompok kecerdasan buatan (Artificial Intelligence) yang mempunyai kemampuan khusus untuk menyelesaikan kondisi permasalahan yang ada. Definisi lain juga mengemukakan bahwa sistem pakar ialah hasil dari pengetahuan pakar dan teknik pencarian. Dalam referensi lainnya mengemukakan, bahwa sistem pakar adalah bagian yang terdapat dalam kecerdasan buatan yang diperuntukan dalam pendiagnosaan kerusakan sistem dan sebagai solusi permasalahan.</p>
                        <p class="text-start">Dalam sistem pakar terdapat 2 jenis penalaran, yaitu Rule Base Reasoning dan Case Base Reasoning. Untuk Rule Base Reasoning merupakan bentuk penalaran yang menggunakan konsep aturan – aturan atau metode yang termasuk dalam Rule Base Reasoning diantaranya : Certainty Factor, Teorema Bayes, Dempster Shafer, Euclidean Probability. Sementara itu untuk Case Base Reasoning adalah bentuk penalaran yang menggunakan teknik kemiripan antara bentuk penalaran yang menggunakan teknik kemiripan antara kasus baru dengan kasus sebelumnya, metode yang termasuk didalamnya adalah K-Nearest Neigbor, Manhatan Distance dan Minkowski Distance. Dalam pengembangannya Sistem Pakar juga mengadopsi logika fuzzy yang dikenal sebagai metode yang digunakan dalam masalah prediksi atau ketidakpastian, ada beberapa metode yang terdapat dalam logika fuzzy, diantaranya : Tsukamoto, Mamdani, Tahani dan Sugeno.</p>
