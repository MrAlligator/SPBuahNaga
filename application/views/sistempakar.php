            <div class="card mb-3">
                <div class="card-header py-3">
                    <h6 class="m-0 font-weight-bold text-primary">Arsitektur Sistem Pakar</h6>
                </div>
                <dl class="row">
                    <div class="card-body">
                        <p class="text-start">Sistem pakar disusun oleh 6 bagian utama, yaitu : basis pengetahuan (knowledge base), motor inferensi (inferece engine), basis data (database spreadsheet), antarmuka pengguna (user interface), fasilitas penjelasan (explanation subsystem), dan pengguna (user). Berikut adalah penjelasan tentang komponen – komponen arsitektur sistem pakar : </p>
                        <ul>
                            <li>Basis Pengetahuan<i>(Knowledge Base)</i></li>
                            <ul class="list-unstyled">
                                <li>Basis pengetahuan mengandung pengetahuan yang diperlukan untuk memahami, memformulasikan, dan menyelesaikan masalah. Basis pengetahuan terdiri dari dua elemen dasar, yaitu Fakta, misalnya situasi, kondisi, atau pemasalahan yang ada, dan
                                    Rule, untuk mengarahkan penggunaan pengetahuan dalam memecahkan masalah.
                                </li>
                            </ul>
                            <li>Basis Data <i>(Database Spreadsheet)</i></li>
                            <ul class="list-unstyled">
                                <li>Digunakan Sebagai Media yang berfungsi untuk menampung fakta – fakta, kondisi yang diperoleh dari basis pengetahuan untuk disimpan dan diproses oleh komputer.
                                </li>
                            </ul>
                            <li>Mesin Inferensi <i>(Inference Engine)</i></li>
                            <ul class="list-unstyled">
                                <li>Merupakan sebuah program yang berfungsi untuk memandu proses penalaran terhadap suatu kondisi berdasarkan pada basis pengetahuan yang ada, memanipulasi dan mengarahkan kaidah, model, dan fakta yang disimpan dalam basis pengetahuan untuk mencapai solusi atau kesimpulan.</li>
                            </ul>
                            <li>Antarmuka Pengguna <i>(User Interface)</i></li>
                            <ul class="list-unstyled">
                                <li>Digunakan sebagai media komunikasi antara pengguna dan sistem pakar.</li>
                            </ul>
                            <li>Fasilitas Penjelasan <i>(Explanation Subsystem / Justifier)</i></li>
                            <ul class="list-unstyled">
                                <li>Berfungsi memberi penjelasan kepada pengguna, bagaimana suatu kesimpulan dapat diambil.</li>
                            </ul>
                            <li>Pengguna <i>(User)</i></li>
                            <ul class="list-unstyled">
                                <li>Pada umumnya pengguna sistem pakar bukanlah seorang pakar <i>(non - expert)</i> yang membutuhkan solusi atau saran dari berbagai permasalahan yang ada.</li>
                            </ul>
                        </ul>
