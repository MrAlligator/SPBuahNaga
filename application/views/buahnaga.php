                    <p class="col-sm-9 justify-content"><br>Buah naga termasuk dalam kelompok tanaman kaktus atau famili <i>Cacatae</i> dan subfamili <i>Hylocereanea</i>.
                        Dalam subfamili ini terdapat beberapa genus, sedangkan buah naga termasuk genus <i>Hylocereus</i>.
                        Genus ini terdiri atas sekitar 16 spesies. Dua di antaranya memiliki buah yang komersial, yaitu <i>Hylocereus undatus</i> (berdaging putih) dan <i>Hylocereus costaricensis</i> (berdaging merah).
                        Daerah asal buah ini adalah Meksiko, Amerika Tengah dan Amerika Utara.
                        Tanaman buah naga lebih dikenal sebagai tanaman dari Asia karena dikembangkan secara besar – besaran di Asia seperti Vietnam dan Thailand.<br>
                        Buah naga mulai dikenal sekitar pertengahan tahun 2000, hasil impor dari Thailand dan mulai dikembangkan di Indonesia pada tahun 2001.
                        Daerah yang mengembangkan buah naga ialah Pasuruan, Jember, Mojokerto,dan Jombang.
                        Agar pertumbuhan perakaran tanaman normal, dianjurkan agar derajat keasaman tanah berada pada kondisi ideal, yaitu pH 6,5 - 7.
                        Curah hujan yang ideal juga akan mempengaruhi pertumbuhan dan perkembangan tanaman, yaitu sekitar 60 mm/bulan atau 720 mm/tahun. Pada curah hujan 600 – 1.300 mm/tahun pun tanaman masih dapat tumbuh.
                        Intensitas cahaya yang disukai oleh tanaman ini berkisar pada 70 – 80 %. Pertumbuhannya pun juga akan lebih baik jika ditanam didataran rendah antara 0 – 350 m dpl, dengan suhu udara antara 26° - 36° C dan kelembapan 70 – 90%.</p>
